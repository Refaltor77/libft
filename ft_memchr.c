#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	int		index;
	char	*cast;

	cast = (char *)s;
	index = 0;
	while (index < (int)n)
	{
		if (cast[index] == (char)c)
			return (cast + index);
		index++;
	}
	return (NULL);
}

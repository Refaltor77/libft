#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*last_lst;

	if (!lst)
		return ;
	if (*lst)
	{
		last_lst = ft_lstlast(*lst);
		last_lst->next = new;
	}
	else
	{
		*lst = new;
	}
}
